# while문
i = 0
while i < 10:
    print(i, end=" ")
    i = i + 1
print()

# for문
for i in range(10):
    print(i, end=" ")
print()

for i in range(1, 11):
    print(i, end=" ")
print()

for i in range(1, 11, 2):
    print(i, end=" ")
print()

lst = [32, 10, 40, 200]
for i in lst:
    print(i, end=" ")
